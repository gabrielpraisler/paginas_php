            <h3>Barramento PCI</h3>
                <p class="black-text">Barramento PCI (Peripheral Component Interconnect), surgiu no início de 1990 pelas mãos da Intel. Suas principais características eram a capacidade de transferir dados a 32 bits e clock de 33 MHz, especificações que tornaram o padrão capaz de transmitir dados a uma taxa de até 132 MB/s.</p>
                <img class="responsive-img" src="https://s2.glbimg.com/I4Se5eES-hh94_pqJwKYASuWEZw=/0x0:620x540/984x0/smart/filters:strip_icc()/i.s3.glbimg.com/v1/AUTH_08fbf48bc0524877943fe86e43087e7a/internal_photos/bs/2021/f/m/cj4wQaT0uZXh1oxwLUfA/2011-12-12-pcie.jpg">

                <h3>Quais os tipos de barramentos externos?</h3>
                <p class="black-text">Uma conexão de barramento externo pode ser direta ou indireta. Para uma conexão de barramento externo direta, as mensagens são roteadas diretamente por meio de um link entre o barramento local e o barramento externo.</p>
                <p class="black-text">Barramento de endereços – indica o local onde os processos devem ser extraídos e para onde devem ser enviados após o processamento. Barramento de controle – atua como um regulador das outras funções, podendo limitá-las ou expandi-las em razão de sua demanda.</p>
                <img class="responsive-img" src="https://tm.ibxk.com.br/materias/12781/51729.jpg?ims=1200x675">

                <h3>O que é barramento externo e interno?</h3>
                <p class="black-text">Um barramento interno pode transportar dados, endereços de memória, informações de controle e quaisquer outras comunicações ou processos internos. Como um barramento interno é interno ao sistema e está instalado de perto, ele permite uma transferência de dados mais rápida do que um barramento externo.</p>
                <p class="black-text">Barramento é um elemento presente no quadro de distribuição responsável pela distribuição de tensão e corrente dentro de um sistema elétrico como, por exemplo, quadros de distribuição.</p>
                <img class="responsive-img" src="https://www.gamerinfo.com.br/wp-content/uploads/2022/09/Capa-1.jpg">

                <h3>Qual é a função do barramento?</h3>
                <p class="black-text">Em arquitetura de computadores, barramento é um conjunto de linhas de comunicação que permitem a interligação entre dispositivos, como a CPU, a memória e outros periféricos.</p>
                <p class="black-text">Junto com os processadores, memória RAM e memória cache, outra classe importante são os barramentos, já que são eles os responsáveis por interligar os diferentes componentes da placa-mãe e permitir o uso de periféricos.</p>
