        <h3>O que são Hardwares?</h3>
        <div class="cards">
            <div class="card-content">
                <p class="black-text">O hardware é uma peça eletrônica que junto a outras compõem um PC. Ele é formado por circuitos elétricos e comandado por um sistema operacional e por softwares, que têm uma função como a de cérebro desse componente.</p>
                <p class="black-text">Os hardwares podem ser internos ou externos, ou seja, podem ser alocados dentro do gabinete de um computador ou na parte de fora. A grande diferença entre eles é que geralmente as peças que compõem a parte interna de um PC são interligadas diretamente à placa mãe e não são trocadas facilmente e no mesmo período do que os que ficam na parte exterior.</p>
                <img class="responsive-img" src="./images/hardwares2.png">

                <h3>Processador (CPU)</h3>
                <p class="black-text">Basicamente, um processador é uma poderosa máquina de calcular. O componente recebe um determinado volume de dados, orientados em padrão binário 0 e 1 e tem a função de responder a esse volume, processando a informação com base em instruções armazenadas em sua memória interna.</p>
                <img class="responsive-img" src="https://geek360.com.br/wp-content/uploads/2020/01/Melhor-processador-para-jogos.jpg">

                <h3>Placa Mãe</h3>
                <p class="black-text">As placas-mãe são desenvolvidas de forma que seja possível conectar todos os dispositivos quem compõem o computador. Para isso, elas oferecem conexões para o processador, para a memória RAM, para o HD, para os dispositivos de entrada e saída, entre outros.</p>
                <img class="responsive-img" src="https://setupgamer.com.br/wp-content/uploads/2021/03/placa-mae-aorus.jpg">

                <h3>Fonte de Energia</h3>
                <p class="black-text">A fonte de energia do computador ou, em inglês, PSU (Power Supply Unit — Unidade de Alimentação de Energia), é responsável por converter a voltagem da energia elétrica, que chega pelas tomadas, em voltagens menores, capazes de ser suportadas pelos componentes do computador.</p>
                <img class="responsive-img" src="https://s.zst.com.br/cms-assets/2022/02/fonte-gamer-capa.webp">

                <h3>HD e SSD</h3>
                <p class="black-text">O HD de computador tem a finalidade de armazenar localmente arquivos, programas, jogos e outros tipos de conteúdos. Todas as informações gravadas em um HD são lidas ou escritas por meio de um processo magnético. Já o SSD armazena dados em células de memória Flash, as mesmas presentes em smartphones e tablets. Cada célula é formada por um controlador, o responsável por fazer a comunicação com o computador, e um transístor de porta flutuante, ou floating gate, que é o que armazena os dados.</p>
                <p class="black-text">Enquanto os discos rígidos (Hard Disk Drive, ou HDD) são formados por partes móveis e mecânicas, os SSDs (Solid State Drives) funcionam com componentes eletrônicos, o que os deixa menores e menos exigentes de energia. Além disso, os HDs são mais frágeis: uma pancada pode tirar as partes móveis do lugar e danificar permanentemente o drive.</p>
                <img class="responsive-img" src="https://tm.ibxk.com.br/2018/08/23/23114946441053.jpg?ims=1120x420">
                <img class="responsive-img" src="https://assets2.rockpapershotgun.com/lol_HAKnl5h.jpg/BROK/thumbnail/1600x900/quality/100/lol_HAKnl5h.jpg">

                <h3>Memória RAM</h3>
                <p class="black-text">A sigla RAM vem do inglês “random access memory”, que pode ser traduzindo para “memória de acesso aleatório”. Esse componente é responsável por dar mais agilidade e velocidade no funcionamento geral do sistema. Sem a memória RAM, tarefas como abrir programas e editar arquivos demorariam muito para serem realizadas.</p>
                <img class="responsive-img" src="https://www.topgadget.com.br/wp-content/uploads/2020/02/memoria-ram.jpg">

                <h3>Placa de Vídeo</h3>
                <p class="black-text">A placa gráfica é a peça do seu computador responsável por gerar as imagens que você vê na tela, seja essa página da web, um documento no Word ou mesmo um filme ou um game. Essa peça também é conhecida como unidade de processamento gráfico (ou GPU), e pode variar de "gráficos integrados" simples, que fazem parte da placa-mãe ou do processador, a placas de expansão maiores e mais poderosas. Essas placas de expansão - geralmente chamadas de placas "dedicadas" - são capazes de executar tarefas mais poderosas do que as placas gráficas integradas, como gráficos de jogos, renderização acelerada de vídeo ou até mesmo trabalhos não gráficos, como mineração de bitcoin.</p>
                <img class="responsive-img" src="https://s.zst.com.br/cms-assets/2021/12/melhor-placa-de-v-deo-barata.webp">

