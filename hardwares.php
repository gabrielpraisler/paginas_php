                <h3>Barramento PCI - Placa Mãe</h3>
                <p class="black-text">Barramento PCI (Peripheral Component Interconnect), surgiu no início de 1990 pelas mãos da Intel. Suas principais características eram a capacidade de transferir dados a 32 bits e clock de 33 MHz, especificações que tornaram o padrão capaz de transmitir dados a uma taxa de até 132 MB/s.</p>
