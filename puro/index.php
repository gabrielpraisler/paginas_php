            <h1>Principais Tags</h1>
            <ul>
                <li>head – local para declarar todas informações, como título e metadados da sua página;</li>
                <li>title – define o título;</li>
                <li>body – local para declarar todos os elementos que irão compor o corpo da página;</li>
                <li>h1,h2,h3,h4,h5 e h6 – Tags para definir um título e subtítulos, variando de 1 a 6, sendo h1 o título mais importante e h6 o de menor importância;</li>
                <li>p – Tag para definir um parágrafo.+;</li>
                <li>a – Tag de link, junto ao atributo href=”” é responsável pela principal característica da web;</li>
                <li>header – define um cabeçalho;</li>
                <li>section – define uma seção;</li>
                <li>article – define um artigo;</li>
                <li>div – define uma divisão;</li>
                <li>footer – define um rodapé;</li>
                <li>nav – define uma área de navegação (como menus);</li>
                <li>table – define uma tabela;</li>
                <li>ol – define uma lista ordenada;</li>
                <li>ul – define uma lista não ordenada;</li>
                <li>li – define o item de uma lista;</li>
                <li>form – define um formulário;</li>
                <li>input – define os campos do formulário;</li>
                <li>textarea – define uma área para o usuário digitar um texto;</li>
                <li>button – define um botão;</li>
                <li>img – permite inserir uma imagem no seu documento.</li>
            </ul>
